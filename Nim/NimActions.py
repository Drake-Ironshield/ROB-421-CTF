class LocateBarrel(StateNode):
    def start(self , event=None):

        super().start(event)
        currentDistance = 0
        lowestDistance = 0 
        closestObj : BarrelObj

        for obj in robot.world_map.objects.values():

            if isinstance(obj , BarrelObj) and obj.is_visible:
                currentDistance = np.sqrt(np.square(robot.pose.x - obj.pose.x) + np.square(robot.pose.y - obj.pose.y))
                if currentDistance < lowestDistance or lowestDistance == 0:
                    closestObj = obj
                    lowestDistance = currentDistance
        if lowestDistance == 0:
            self.post_failure()
        else:
            self.post_data(closestObj)

