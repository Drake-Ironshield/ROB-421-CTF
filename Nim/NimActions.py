class ArucoName(StateNode):
    def start(self , event=None):
        super().start(event)
        markerFound = 0
        for obj in robot.world_map.objects.values():
            if isinstance(obj , ArucoMarkerObj) and obj.marker_id == 3:
                self.post_data(obj)
                markerFound = 1

        if markerFound == 0:
            self.post_failure()

class ArucoDistance(StateNode):
    def start(self , event=None):
        super().start(event)
        distanceX = 0
        distanceY = 0
        markerFound = 0
        for obj in robot.world_map.objects.values():
            if isinstance(obj , ArucoMarkerObj) and obj.marker_id == 3:
                distanceX = obj.pose.x - robot.pose.x
                distanceY = obj.pose.y - robot.pose.y 
                distance = np.sqrt(distanceX ** 2 + distanceY ** 2)
                self.post_data(distance)

        if markerFound == 0:
            self.post_failure()

